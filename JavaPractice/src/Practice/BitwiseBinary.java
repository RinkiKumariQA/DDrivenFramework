package Practice;

public class BitwiseBinary {

	public static void main(String[] args) 
	{
        int x;
        x=48>>2;
        System.out.println("x="+x);
        
        int y;
        y=64<<2;
        System.out.println("y="+y);
        
        int z;
        z=45<<2;
        System.out.println("z="+z);
	}

}
